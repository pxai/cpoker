void deckinit (void)
{
	DECKINDEX = 0;
	int type[4] = {1,2,3,4};
	int i,t, value;
	i = t = value = 0;

	for (i=0;i<DECKSIZE;i++)	
	{
		t = (i/13) + 1;
		value = (i%13);
		POKERDECK[i].value = value;
		POKERDECK[i].type = type[t];
		POKERDECK[i].show = IMAGES[i];
	}

}

